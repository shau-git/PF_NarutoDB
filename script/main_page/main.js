let clickCount = 1;
document.querySelector('.header-title').addEventListener('click', () => {
    //document.getElementById('change-img-btn').play()

    if (clickCount > 18) {
        clickCount = 0
    }

    // after each click remove all the previous display
    reset()
    document.querySelector('.cover-img').src= `${data[clickCount].images}`
    if (data[clickCount].sounds) {
        createAudio(data[clickCount].sounds)
        soundBtn(data[clickCount].soundsId)
    }
    
    if (data[clickCount].quote) {
        createQuote(data[clickCount].quote)
    }
    clickCount += 1;
})

//rendering the image
function renderImage(data) {
    const image = document.createElement('img')
    image.classList.add('cover-img')
    image.src = data
}


//create audio
function createAudio(sounds) {
    let i = 0;
    sounds.forEach(sound => {
        const audio = document.createElement('audio')
        audio.id = data[clickCount].soundsId[i]
        i++
        audio.src= sound
        document.getElementById('sound-btn-container').appendChild(audio)
    })
}


//creating  button to play the sounds
function soundBtn(soundsId) {
    soundsId.forEach(sound => {
        const btn = document.createElement('button')
        btn.classList.add('btn')
        btn.innerText = sound
        btn.addEventListener('click', () => {
            stopSongs(soundsId)
            document.getElementById(sound).play()
        })
        document.getElementById('sound-btn-container').appendChild(btn)
    })
}


function stopSongs(soundsId) {
    soundsId.forEach( sound => {
        const song = document.getElementById(sound)
        song.pause()
        song.currentTime = 0;
    })
}

function createQuote(quotes) {
    quotes.forEach(quote => {
        const quoteRender = document.createElement('p')
        quoteRender.innerText = quote
        document.getElementById('quote').appendChild(quoteRender)
    })
}




function reset() {
    const quote = document.getElementById('quote');
    const audio = document.getElementById('sound-btn-container')
    const img = document.getElementsByClassName('cover-img-container')[0]

    //reset quote
    while(quote.firstChild){
        quote.removeChild(quote.firstChild)
    }

    //reset audios
    while(audio.firstChild){
        audio.removeChild(audio.firstChild)
    }
}


//this function is only used just for when loading the page for the sound button
function sound() {
    document.getElementById('infinite-tsukuyomi').play()
}

